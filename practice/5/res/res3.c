#include <stdio.h>

int main()
{
    printf("%f\n", 30.0 / 4.0 * 5.0);     // 37.500000
    printf("%f\n", 30.0 / (4.0 * 5.0)); // 1.500000
    printf("%f\n", 30 / 4 * 5); // 1.500000
    printf("%f\n", 30 * 5 / 4); // 1.500000
    printf("%f\n", 30 / 4.0 * 5); // 37.500000
    printf("%f\n", 30 / 4 * 5.0); // 35.000000
    
    return 0;
}
